package shadow.leetcode.util;

public class IntListNode {
    public int val;
    public IntListNode next;

    public IntListNode() {
    }

    public IntListNode(int val) {
        this.val = val;
    }

    public IntListNode(int val, IntListNode next) {
        this.val = val;
        this.next = next;
    }
}
